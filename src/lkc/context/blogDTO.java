package lkc.context;

public class blogDTO {
	private String blog_id;
	private String blog_num;
	private String blog_title;
	private String blog_visible;
	private String blog_level1;
	private String blog_level2;
	public String getBlog_id() {
		return blog_id;
	}
	public void setBlog_id(String blog_id) {
		this.blog_id = blog_id;
	}
	public String getBlog_num() {
		return blog_num;
	}
	public void setBlog_num(String blog_num) {
		this.blog_num = blog_num;
	}
	public String getBlog_title() {
		return blog_title;
	}
	public void setBlog_title(String blog_title) {
		this.blog_title = blog_title;
	}
	public String getBlog_visible() {
		return blog_visible;
	}
	public void setBlog_visible(String blog_visible) {
		this.blog_visible = blog_visible;
	}
	public String getBlog_level1() {
		return blog_level1;
	}
	public void setBlog_level1(String blog_level1) {
		this.blog_level1 = blog_level1;
	}
	public String getBlog_level2() {
		return blog_level2;
	}
	public void setBlog_level2(String blog_level2) {
		this.blog_level2 = blog_level2;
	}

	
}
